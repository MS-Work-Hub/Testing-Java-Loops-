package Loops;

public class WhileLoop {

	public static void main(String[] args) {
		
		//I want to print table of 4
		
		int t=4;                           //Initialization
		
		while (t<=40)                      //Condition
		{
			System.out.println(t);
			t=t+4;                         //Updation	
		}
		
		
		
		//I want to print eclipse 5 times
		
		String name="eclipse";
		int n=1;
		while (n<=5)
		{
			System.out.println(name);
			n=n+1;
		}
			

	}

}
