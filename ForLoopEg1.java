package Loops;

public class ForLoopEg1 {

	public static void main(String[] args) {
		
	// Get sum of 1 to 10 Natural numbers
		
	int sum = 0;
		
	for (int i = 1; i<=10; i++) {
		
	sum = sum + i;                          //Repeated (Pre. sum + next value of(i)) 
	
	}
		System.out.println("The sum of 1-10 numbers is "+sum);
		System.out.println("*******************************");
		
	
	
	
	for (int i=0; i<=10; i++)
	{
		System.out.print(i+" ");
	}
}
}




